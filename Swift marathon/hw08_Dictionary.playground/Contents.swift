//Создайте дикшинари как журнал студентов, где имя и фамилия студента это ключ, а оценка за контрольную значение.

var students = [
    "Arnold Ivanov": 5,
    "Silvestr Petrov": 4,
    "Stiven Sidorov": 3,
    "Jackie Balakin": 4,
    "Vladimir Chugunov": 5
]

//Некоторым студентам повысьте оценки – они пересдали.

students["Silvestr Petrov"] = 5
students["Stiven Sidorov"] = 4

//Потом добавьте парочку студентов, так как их только что перевели к вам в группу.

students["Vinogradov Artem"] = 3
students["Silaev Alexsander"] = 5

//А потом несколько удалите, так как они от вас ушли.

students.removeValue(forKey: "Arnold Ivanov")
students.removeValue(forKey: "Silvestr Petrov")

//После всех этих перетрубаций посчитайте общий бал группы

let sumMarks = students
    .values
    .reduce(into: 0) { result, mark in
        result += mark
    }

sumMarks

//и средний бал

let markGroup = Double(sumMarks) / Double(students.count)

