//Создать дикшинари , в которой ключ это адрес шахматной клетки (пример: a5, b3, g8), а значение это Bool. Если у клетки белый цвет, то значение true, а если черный – false. Выведите дикшинари в печать и убедитесь что все правильно. Рекомендация: постарайтесь все сделать используя вложенный цикл (объяснение в уроке).

var chessBoard = [String: Bool]()

let letters = ["a", "b", "c", "d", "e", "f", "g", "h"]
for (index, value) in letters.enumerated() {
    let coordX = index + 1
    for coordY in 1...8 {
        if (coordX + coordY) % 2 == 0 {
            chessBoard["\(value)\(coordY)"] = false
        } else {
            chessBoard["\(value)\(coordY)"] = true
        }
    }
}

chessBoard
chessBoard.count
for (key, value) in chessBoard {
    print("Key = \(key), value = \(value)")
}

